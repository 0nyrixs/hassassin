def links_dump(url):
    datos = links = []
    linkos = files = []

    try:
        html = requests.get(url)
        soup = str(BeautifulSoup(html.text, 'lxml').extract())

        datass = soup.split('\n')

        datos = p.findall(str(datass))
        linkos = q.findall(str(datos))

        for elem in datos:
            for element in elem:
                if "https://" in element or "http://" in element:
                    links.append(element)

        for elem in linkos:
            for element in elem:
                if "https://" in element or "http://" in element:
                    files.append(element)

        return datos, linkos

    except TimeoutException:
        print('Timeout Received')
        return None


